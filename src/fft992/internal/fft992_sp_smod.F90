SUBMODULE (FFT992_MOD) FFT992_SP_SMOD

CONTAINS

#undef SET99B
#undef FFT992_CC
#undef FFT992
#undef FFT992_PRECISION
#define SET99B    SET99B_SP_IMPL
#define FFT992_CC FFT992_CC_SP_IMPL
#define FFT992    FFT992_SP_IMPL
#define FFT992_PRECISION REAL32

#include "set99b.F90"
#include "fft992_cc.F90"
#include "fft992_impl.F90"

MODULE PROCEDURE SET99B_SP
  CALL SET99B_SP_IMPL(TRIGS, IFAX, N, LDUSEFFT992)
END PROCEDURE SET99B_SP

MODULE PROCEDURE FFT992_CC_SP_1D
  CALL FFT992_CC_SP_IMPL(A, KINC, KJUMP, KN, KLOT, KSIGN)
END PROCEDURE FFT992_CC_SP_1D

MODULE PROCEDURE FFT992_CC_SP_2D
  USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY : REAL32
  REAL(KIND=REAL32), POINTER :: A_RANK1(:)
  A_RANK1(1:SIZE(A)) => A
  CALL FFT992_CC_SP_IMPL(A_RANK1, KINC, KJUMP, KN, KLOT, KSIGN)
END PROCEDURE FFT992_CC_SP_2D

MODULE PROCEDURE FFT992_SP_1D
  CALL FFT992_SP_IMPL(A, TRIGS, IFAX, INC, JUMP, N, LOT, ISIGN, PSCALE)
END PROCEDURE FFT992_SP_1D

MODULE PROCEDURE FFT992_SP_2D
  USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY : REAL32
  REAL(KIND=REAL32), POINTER :: A_RANK1(:)
  A_RANK1(1:SIZE(A)) => A
  CALL FFT992_SP_IMPL(A_RANK1, TRIGS, IFAX, INC, JUMP, N, LOT, ISIGN, PSCALE)
END PROCEDURE FFT992_SP_2D

END SUBMODULE FFT992_SP_SMOD